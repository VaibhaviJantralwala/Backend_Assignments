public class DataTypes{
    public static void main(String[] args){

        // a program to demonstrate the use of different data types.

        int a = 10;
        long l = 100000000;
        float f = 1.2f;
        double d = 12.23456;
        char c = 'v';
        boolean flag = true;

        System.out.println("Integer Datatype : "+a+"\n"+"Long Datatype : "+l+"\n"+"Float Datatype : "+f+"\n"+"Double Datatype : "+d+"\n"+"Character Datatype : "+c+"\n"+"Boolean Datatype : "+flag);     
    }
}

// output :
// Integer Datatype : 10
// Long Datatype : 100000000
// Float Datatype : 1.2
// Double Datatype : 12.23456
// Character Datatype : v
// Boolean Datatype : true

